function capitalize(str) {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

export function simulateRecipe(ingredients) {
  const lower = ingredients.map((i) => i.toLowerCase());

  let bestMatch = null;
  let bestScore = 0;

  for (const entry of recipes) {
    const score = entry.match.filter((m) =>
      lower.some((ing) => ing.includes(m) || m.includes(ing))
    ).length;
    if (score > bestScore) {
      bestScore = score;
      bestMatch = entry.recipe;
    }
  }

  if (bestMatch && bestScore >= 1) return { ...bestMatch };

  return {
    name: `${capitalize(ingredients[0])} ${["Medley", "Bowl", "Skillet"][Math.floor(Math.random() * 3)]}`,
    description: `A creative dish combining ${ingredients.slice(0, 3).join(", ")} with bold seasoning and simple technique.`,
    cuisine: "Fusion",
    cooking_time: "30 minutes",
    servings: 2,
    ingredients: ingredients.map((i, idx) => {
      const amounts = ["200g", "1 cup", "2 tbsp", "3 cloves", "1 large", "100g"];
      return `${amounts[idx % amounts.length]} ${i}`;
    }).concat(["2 tbsp oil", "Salt and pepper to taste"]),
    steps: [
      `Wash and prepare all ingredients. Dice ${ingredients[0]} into even pieces`,
      "Heat oil in a large pan over medium-high heat",
      `Add ${ingredients[0]} and cook 3-4 minutes until starting to brown`,
      ingredients.length > 1 ? `Add ${ingredients.slice(1).join(", ")} and cook 3 more minutes` : "Season generously with your favorite spices",
      "Adjust seasoning with salt and pepper",
      "Serve hot with rice or bread",
    ],
  };
}