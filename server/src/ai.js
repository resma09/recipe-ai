const DEFAULT_FOOD_IMAGE = "https://images.unsplash.com/photo-1495521821757-a1efb6729352?w=600&q=80";

// ── UNSPLASH IMAGE FETCH ──
export async function fetchFoodImage(recipeName) {
  const unsplashKey = process.env.UNSPLASH_ACCESS_KEY;
  if (!unsplashKey) return { url: DEFAULT_FOOD_IMAGE, credit: null };

  try {
    const query = encodeURIComponent(`${recipeName} food dish`);
    const res = await fetch(
      `https://api.unsplash.com/search/photos?query=${query}&per_page=1&orientation=landscape`,
      { headers: { Authorization: `Client-ID ${unsplashKey}` } }
    );
    if (!res.ok) throw new Error(`Unsplash: ${res.status}`);
    const data = await res.json();
    if (data.results?.length > 0) {
      const photo = data.results[0];
      return { url: `${photo.urls.regular}&w=600&q=80`, credit: photo.user?.name || null };
    }
    return { url: DEFAULT_FOOD_IMAGE, credit: null };
  } catch (err) {
    console.warn("[Image] Unsplash failed:", err.message);
    return { url: DEFAULT_FOOD_IMAGE, credit: null };
  }
}

// ── GEMINI FLASH IMAGE GENERATION (REST API) ──
export async function generateRecipeImage(recipeName, ingredients) {
  const apiKey = process.env.GEMINI_API_KEY;
  // *** FIXED: Updated to the stable model name ***
  const url = `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash-image:generateContent?key=${apiKey}`;

  const prompt = `Professional food photography of "${recipeName}". 
  Main ingredients: ${ingredients.slice(0, 4).join(", ")}.
  Style: bright natural lighting, top-down view, vibrant colors, restaurant quality, 4K.`;

  try {
    const response = await fetch(url, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        contents: [{ parts: [{ text: prompt }] }],
        generationConfig: {
          responseModalities: ["TEXT", "IMAGE"]
        }
      })
    });

    if (!response.ok) {
      const errorText = await response.text();
      throw new Error(`HTTP ${response.status}: ${errorText}`);
    }

    const data = await response.json();
    const part = data.candidates?.[0]?.content?.parts?.find(p => p.inlineData);

    if (part?.inlineData) {
      const base64 = part.inlineData.data;
      const mimeType = part.inlineData.mimeType || "image/png";
      return {
        url: `data:${mimeType};base64,${base64}`,
        credit: "Generated by Gemini AI"
      };
    }
    throw new Error("No image data in response");
  } catch (err) {
    console.error("[Gemini Image] Failed:", err.message);
    return null; // Your existing fallback to Unsplash will handle this
  }
}

export async function generateRecipeImageImagen(recipeName, ingredients) {
  const apiKey = process.env.GEMINI_API_KEY; // same key works for Imagen
  const url = "https://generativelanguage.googleapis.com/v1beta/models/imagen-4.0-generate-001:predict";

  const prompt = `Professional food photography of "${recipeName}" with ${ingredients.slice(0, 3).join(", ")}. Plated beautifully, natural lighting, high resolution, 4K.`;

  const response = await fetch(url, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      "x-goog-api-key": apiKey,
    },
    body: JSON.stringify({
      instances: [{ prompt }],
      parameters: {
        sampleCount: 1,
        aspectRatio: "1:1",
      },
    }),
  });

  const data = await response.json();
  if (data.predictions && data.predictions[0]) {
    const base64 = data.predictions[0].bytesBase64Encoded;
    return {
      url: `data:image/png;base64,${base64}`,
      credit: "Generated by Imagen 4.0",
    };
  }
  throw new Error("Imagen failed");
}

// ── GEMINI AI (FREE) ──
let _gemini;
function getGemini() {
  if (!_gemini) {
    const apiKey = process.env.GEMINI_API_KEY;
    if (!apiKey) throw new Error("GEMINI_API_KEY not set");
    _gemini = new GoogleGenAI({ apiKey });
  }
  return _gemini;
}

export async function generateRecipe(ingredients) {
  const ingredientList = ingredients.join(", ");

  const response = await getGemini().models.generateContent({
    model: "gemini-2.5-flash",
    contents: `I have these ingredients: ${ingredientList}

Suggest a recipe I can make with these ingredients (you can assume I have basic pantry staples like salt, pepper, oil, etc.).

Respond ONLY with a JSON object in this exact format, no markdown, no backticks, no other text:
{
  "name": "Recipe Name",
  "description": "A brief 1-2 sentence description of the dish",
  "cuisine": "Cuisine type (e.g. Italian, Indian, Mexican, Nepali, American)",
  "cooking_time": "Estimated time (e.g. 30 minutes, 1 hour)",
  "servings": 2,
  "ingredients": ["ingredient 1 with amount", "ingredient 2 with amount"],
  "steps": ["Step 1 instruction", "Step 2 instruction", "Step 3 instruction"]
}`,
  });

  const text = response.text;
  const cleaned = text.replace(/```json\s*/g, "").replace(/```\s*/g, "").trim();

  try {
    return JSON.parse(cleaned);
  } catch (parseError) {
    console.error("[AI] Failed to parse Gemini response:", text);
    throw new Error("Failed to parse recipe from AI response");
  }
}

